public class Q02_Println_vs_print {

    /*

    println() ve print() arasındaki fark
  println() alt alta yazar
  print() aynı satıra yazar


     */

    public static void main(String[] args) {

        System.out.println("****** println li kullanım ***** ");
        System.out.println("welcome");
        System.out.println("java");
        System.out.println("practice");

        System.out.println("****** print li kullanım ***** ");

        System.out.print("welcome ");
        System.out.print("java ");
        System.out.print("practice");



    }
}
